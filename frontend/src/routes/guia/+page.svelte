            <h1>Guia do Ecossistema Dólarize 2.0</h1>
            <p>Bem-vindo ao seu novo funcionário digital. O Dólarize 2.0 é um sistema autônomo projetado para captar, qualificar e educar seus clientes 24 horas por dia.</p>

            <h2>PARTE 1: Os 3 Pilares do "Cérebro" da IA</h2>
            <p>Você tem o controle total de como o agente pensa e age gerenciando três pilares no seu painel:</p>
            <ol>
                <li><strong>Fonte da Verdade (Base de Conhecimento):</strong> Evita que a IA invente informações. Faça o upload de seus PDFs ou arquivos do Word. A IA só usa o que está aqui para responder dúvidas técnicas.</li>
                <li><strong>Tom de Voz (Centro de Personalidade):</strong> Define a "alma" do agente. Digite instruções de comportamento (ex: "Use linguagem simples e emojis").</li>
                <li><strong>Regras de Ouro (Configurações Nucleares):</strong> O seu escudo jurídico. São ordens absolutas que a IA nunca desobedecerá. Edite o texto das regras a qualquer momento (ex: "Nunca dê conselhos de investimento").</li>
            </ol>

            <h2>PARTE 2: CRM e Operação de Vendas</h2>
            <p><strong>Qualificação Invisível (Dashboard):</strong> Durante o chat, a IA extrai o Nome e E-mail do cliente e classifica o lead como Perfil A (Pronto), B (Inseguro) ou C (Curioso).</p>
            <p><strong>Máquina de Objeções (Central de Vídeos):</strong> Adicione links do YouTube e defina um "Gatilho". Quando o cliente tocar nesse assunto, a IA recomendará seu vídeo.</p>
            <p><strong>O Fechamento (Human Handoff):</strong> Encontrou um "Perfil A" quente na tabela? Ative a opção Pausar IA. O robô silencia e você assume a conversa pessoalmente para fechar o contrato.</p>
